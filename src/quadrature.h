#ifndef BEM_QUADRATURE_H
#define BEM_QUADRATURE_H

// Dunavant quadrature rules for triangles.
// Points are barycentric coordinates (lambda1, lambda2), lambda0 = 1 - lambda1 - lambda2.
// Weights sum to 1.0 (= integral over reference triangle with area 1).

struct TriQuad {
    int npts;
    double pts[25][2];  // (lambda1, lambda2)
    double wts[25];
};

inline TriQuad tri_quadrature(int order) {
    TriQuad q;
    if (order == 4) {
        q.npts = 4;
        q.pts[0][0] = 1.0/3; q.pts[0][1] = 1.0/3;
        q.pts[1][0] = 0.6;   q.pts[1][1] = 0.2;
        q.pts[2][0] = 0.2;   q.pts[2][1] = 0.6;
        q.pts[3][0] = 0.2;   q.pts[3][1] = 0.2;
        q.wts[0] = -27.0/48; q.wts[1] = 25.0/48;
        q.wts[2] = 25.0/48;  q.wts[3] = 25.0/48;
    } else if (order == 7) {
        q.npts = 7;
        double a1 = 0.059715871789770, a2 = 0.470142064105115;
        double b1 = 0.797426985353087, b2 = 0.101286507323456;
        q.pts[0][0] = 1.0/3; q.pts[0][1] = 1.0/3;
        q.pts[1][0] = a1;    q.pts[1][1] = a2;
        q.pts[2][0] = a2;    q.pts[2][1] = a1;
        q.pts[3][0] = a2;    q.pts[3][1] = a2;
        q.pts[4][0] = b1;    q.pts[4][1] = b2;
        q.pts[5][0] = b2;    q.pts[5][1] = b1;
        q.pts[6][0] = b2;    q.pts[6][1] = b2;
        q.wts[0] = 0.225;
        q.wts[1] = 0.13239415278851; q.wts[2] = 0.13239415278851; q.wts[3] = 0.13239415278851;
        q.wts[4] = 0.12593918054483; q.wts[5] = 0.12593918054483; q.wts[6] = 0.12593918054483;
    } else if (order == 13) {
        q.npts = 13;
        double pts13[][2] = {
            {1.0/3, 1.0/3},
            {0.260345966079038, 0.260345966079038},
            {0.260345966079038, 0.479308067841923},
            {0.479308067841923, 0.260345966079038},
            {0.065130102902216, 0.065130102902216},
            {0.065130102902216, 0.869739794195568},
            {0.869739794195568, 0.065130102902216},
            {0.048690315425316, 0.312865496004875},
            {0.312865496004875, 0.048690315425316},
            {0.638444188569809, 0.312865496004875},
            {0.312865496004875, 0.638444188569809},
            {0.048690315425316, 0.638444188569809},
            {0.638444188569809, 0.048690315425316},
        };
        double wts13[] = {
            -0.149570044467670,
            0.175615257433204, 0.175615257433204, 0.175615257433204,
            0.053347235608839, 0.053347235608839, 0.053347235608839,
            0.077113760890257, 0.077113760890257, 0.077113760890257,
            0.077113760890257, 0.077113760890257, 0.077113760890257,
        };
        for (int i = 0; i < 13; i++) {
            q.pts[i][0] = pts13[i][0]; q.pts[i][1] = pts13[i][1];
            q.wts[i] = wts13[i];
        }
    } else { // default 7
        return tri_quadrature(7);
    }
    return q;
}

#endif // BEM_QUADRATURE_H
